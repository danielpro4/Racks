module latte{

    /**
     * Enumerates week days
     */
    export enum WeekDay{
        /**
         * Sunday
         *
         * @type {number}
         */
        SUNDAY = 0,

        /**
         * Monday
         *
         * @type {number}
         */
        MONDAY = 1,

        /**
         * Tuesday
         *
         * @type {number}
         */
        TUESDAY = 2,

        /**
         * Wednesday
         *
         * @type {number}
         */
        WEDNESDAY = 3,

        /**
         * Thursday
         *
         * @type {number}
         */
        THURSDAY = 4,

        /**
         * Friday
         *
         * @type {number}
         */
        FRIDAY = 5,

        /**
         * Saturday
         *
         * @type {number}
         */
        SATURDAY = 6,
    }

}